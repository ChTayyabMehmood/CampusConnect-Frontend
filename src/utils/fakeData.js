// Helper function to get opportunity by ID
export const getOpportunityById = (id) => {
  return opportunities.find((opp) => opp.id === id);
};

// Helper function to get related opportunities
export const getRelatedOpportunities = (id) => {
  const opportunity = getOpportunityById(id);
  if (!opportunity || !opportunity.relatedOpportunities) return [];
  return opportunity.relatedOpportunities
    .map((relId) => getOpportunityById(relId))
    .filter(Boolean);
};

// Helper function to get opportunities by type
export const getOpportunitiesByType = (type) => {
  return opportunities.filter((opp) => opp.type === type);
};

// Helper function to get opportunities by status
export const getOpportunitiesByStatus = (status) => {
  return opportunities.filter((opp) => opp.status === status);
};

// Helper function to get featured opportunities
export const getFeaturedOpportunities = (limit = 4) => {
  return opportunities.slice(0, limit);
};

export const getTitlebyid = (id) => {
  const opp = opportunities.find((opp) => opp.id === id);
  return opp.title;
};

